export const getData = () => {
    const planStartTime = '2023-01-04'
    const planEndTime = '2023-09-28'
    const dayCount = dayjs(planEndTime).diff(planStartTime, 'day')

    const rows = 40
    const datas = []
    for (let i = 0; i < rows; i++) {
        let start = Math.ceil(Math.random() * (dayCount - 1 + 1) + 1 - 1)

        const end = Math.ceil(Math.random() * (dayCount - start + 1) + start - 1)
        if (start == end) {
            start -= 1
        }
        // console.log(`-------${start}${end}`)
        datas.push({
            id: `id_${i}`,
            name: `我是${i}`,
            planStartTime: dayjs(planStartTime).add(start, 'day').format('YYYY-MM-DD'),
            planEndTime: dayjs(planStartTime).add(end, 'day').format('YYYY-MM-DD'),
        })
    }
    return {
        planStartTime: planStartTime,
        planEndTime: planEndTime,
        datas: datas,
    }
}
